    private void saveCurWorkoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveCurWorkoutActionPerformed
        // TODO add your handling code here:
        
        Date today = Calendar.getInstance().getTime();
        
        String tDay = new SimpleDateFormat("MM_dd_yyyy").format(today);
        File test = new File("saves/"+tDay);
        boolean ow = overWriteExisting.isSelected();
        if((!test.exists()|| ow)&&newDay.numSets>0){
            System.out.println("Workout saved"+ tDay);
        
        // Write to disk with FileOutputStream
        FileOutputStream f_out = null;
        try {
            f_out = new FileOutputStream("saves/"+tDay);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

        // Write object with ObjectOutputStream
        ObjectOutputStream obj_out = null;
        try {
            obj_out = new ObjectOutputStream (f_out);
        } catch (IOException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            // Write object out to disk
            obj_out.writeObject ( newDay );
        } catch (IOException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        else{
            System.out.println("Save action not performed");
        }
    }//GEN-LAST:event_saveCurWorkoutActionPerformed

    private void loadPrevWorkoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadPrevWorkoutActionPerformed
        // TODO add your handling code here:
        
        Date tmp = theDate.getDate();
        String tDay = new SimpleDateFormat("MM_dd_yyyy").format(tmp);
        File file = new File("saves/"+tDay);
        // Read from disk using FileInputStream
        if(file.exists()){
        FileInputStream f_in = null;
        try {
            f_in = new FileInputStream("saves/"+tDay);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

        // Read object using ObjectInputStream
        ObjectInputStream obj_in = null;
        try {
            obj_in = new ObjectInputStream (f_in);
        } catch (IOException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

        // Read an object
        Object obj = null;
        try {
            obj = obj_in.readObject();
        } catch (IOException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

            if (obj instanceof Workout)
            {
                    // Cast object to a Vector
                    Workout load = (Workout) obj;
                    newDay = load;
                    // Do something with vector....
                    toSArea.setText(newDay.toString());
                    System.out.println(newDay.toString());
            }
        }
        else{
            toSArea.setText("The date you have selected currently has no workout saved!");
        }
    }//GEN-LAST:event_loadPrevWorkoutActionPerformed

