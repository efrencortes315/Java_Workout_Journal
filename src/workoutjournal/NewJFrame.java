    private void addSetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSetButtonActionPerformed
        // TODO add your handling code here:
        
        
        boolean isReady;
        
        if(exName.getText().equals("")){
            isReady=false;       
            System.out.println("Please make sure all fields are filled out");
        }else if(weight.getText().equals("")){
            isReady=false;
            System.out.println("Please make sure all fields are filled out");
        }else if(reps.getText().equals("")){
            isReady=false;
            System.out.println("Please make sure all fields are filled out");
        }else
            isReady = true;
        if(isReady){  
        String ex = exName.getText();
        int wt = Integer.parseInt(weight.getText());
        int rps = Integer.parseInt(reps.getText());
        Motion a = new Motion(ex,wt,rps);
        newDay.addSet(a);
        exName.setText("");
        weight.setText("");
        reps.setText("");
        toSArea.setText(newDay.toString());
        System.out.println("Set added");
        }
        
    }//GEN-LAST:event_addSetButtonActionPerformed

